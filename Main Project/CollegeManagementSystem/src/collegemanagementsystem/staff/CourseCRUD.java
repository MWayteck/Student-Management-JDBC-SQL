    public void loadJTable() {
        DefaultTableModel model = (DefaultTableModel) jTable1.getModel();
        List<CourseModel> list = db.getAllCourses();
        Object rowData[] = new Object[6];
        for (int i = 0; i < list.size(); i++) {
            rowData[0] = list.get(i).getCourseID();
            rowData[1] = list.get(i).getCourseName();
            rowData[2] = list.get(i).getMeets_at();
            rowData[3] = list.get(i).getRoom();
           rowData[4] = list.get(i).getLimit();
           rowData[5] = list.get(i).getFacultyID();
            model.addRow(rowData);
        }
    }
