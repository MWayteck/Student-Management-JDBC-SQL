    private void jTable1MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jTable1MouseClicked
        // TODO add your handling code here:
        row = jTable1.getSelectedRow();
        DefaultTableModel model = (DefaultTableModel) jTable1.getModel();

        idTextField.setText(model.getValueAt(row, 0).toString());
        nameTextField.setText(model.getValueAt(row, 1).toString());
        meetatTextField.setText(model.getValueAt(row, 2).toString());
    }//GEN-LAST:event_jTable1MouseClicked

    private void addCoursetBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_addCoursetBtnActionPerformed
        // TODO add your handling code here:
        String ID = idTextField.getText().toString();
        String Name = nameTextField.getText().toString();
        String meetat = meetatTextField.getText().toString();
        String room = roomTextField.getText().toString();
        String limit = limitTextField.getText().toString();
        String fid = fidTextField.getText().toString();

        DefaultTableModel model = (DefaultTableModel) jTable1.getModel();
        if (ID.isEmpty() || Name.isEmpty() || meetat.isEmpty() || room.isEmpty() || limit.isEmpty() || fid.isEmpty()) {
            JOptionPane.showMessageDialog(null, "Empty Data Not Acceptable");
        } else {
            int lim = Integer.parseInt(limit);
            int fidInt = Integer.parseInt(fid);
            
            new DatabaseHandler().addCourse(new CourseModel(ID, Name, meetat, room,lim, fidInt));
            model.addRow(new Object[]{ID, Name, meetat, room,limit,fidInt });
            JOptionPane.showMessageDialog(null, "Course Added Successfully !");
        }
    }//GEN-LAST:event_addCoursetBtnActionPerformed

    private void deleteCourseBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_deleteCourseBtnActionPerformed
        // TODO add your handling code here:
        String ID = idTextField.getText().toString();
        if(ID.isEmpty()){
            JOptionPane.showMessageDialog(null, "Empty Data Acceptable !");
        }else{
            new DatabaseHandler().deleteCourse(ID);
            JOptionPane.showMessageDialog(null, "Course Deleted Successfully !");
            DefaultTableModel model = (DefaultTableModel) jTable1.getModel();
            model.removeRow(row);

        }
    }//GEN-LAST:event_deleteCourseBtnActionPerformed

    private void updateCoursetBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_updateCoursetBtnActionPerformed
        // TODO add your handling code here:

        String ID = idTextField.getText().toString();
        String Name = nameTextField.getText().toString();
        String meetat = meetatTextField.getText().toString();
        String room = roomTextField.getText().toString();
        String limit = limitTextField.getText().toString();
        String fid = fidTextField.getText().toString();

        DefaultTableModel model = (DefaultTableModel) jTable1.getModel();
        model.removeRow(row);

        model.addRow(new Object[]{ID, Name, meetat, room,limit,fid });

        int lim = Integer.parseInt(limit);
        int fidInt = Integer.parseInt(fid);

        if (ID.isEmpty() || Name.isEmpty() || meetat.isEmpty() ||room.isEmpty()|| limit.isEmpty() || fid.isEmpty()) {
            JOptionPane.showMessageDialog(null, "Empty Data Acceptable !");
        } else {
            new DatabaseHandler().updateCourse(new CourseModel(ID,Name, meetat,room, lim, fidInt));
            JOptionPane.showMessageDialog(null, "Course Updated Successfully !");
        }
    }//GEN-LAST:event_updateCoursetBtnActionPerformed

    private void searchActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_searchActionPerformed
        // TODO add your handling code here:
        String id = idTextField.getText().toString();
        if(!id.isEmpty()){
           CourseModel cm = db.searchCourse(id);
            if(cm!=null)
                JOptionPane.showMessageDialog(this, cm.toString());
            else
                JOptionPane.showMessageDialog(this, "No Record Found");
        } else {
            JOptionPane.showMessageDialog(this, "Enter Valid Student ID");
            
        }
    }//GEN-LAST:event_searchActionPerformed

