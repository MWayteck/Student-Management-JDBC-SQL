    private void searchBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_searchBtnActionPerformed
        // TODO add your handling code here:
        String id = studentIDTextField.getText().toString();
        if(!id.isEmpty()){
            Enrollment em = db.searchEnrollement(Integer.parseInt(id));
            if(em!=null)
                JOptionPane.showMessageDialog(this, em.toString());
            else
                JOptionPane.showMessageDialog(this, "No Record Found");
        } else {
            JOptionPane.showMessageDialog(this, "Enter Valid Student ID");
            
        }
    }//GEN-LAST:event_searchBtnActionPerformed

