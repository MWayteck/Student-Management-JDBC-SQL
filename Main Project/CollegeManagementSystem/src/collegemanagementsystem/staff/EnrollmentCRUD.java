      public void loadJTable() {
        DefaultTableModel model = (DefaultTableModel) jTable1.getModel();
        List<Enrollment> list = db.getAllEnrolledStudents();
        Object rowData[] = new Object[5];
        for (int i = 0; i < list.size(); i++) {
            rowData[0] = list.get(i).getSid();
            rowData[1] = list.get(i).getCid();
            rowData[2] = list.get(i).getExam1();
            rowData[3] = list.get(i).getExam2();
           rowData[4] = list.get(i).getFinals();
            model.addRow(rowData);
        }
    }

